#include<stdio.h>
int main()
{
	int i,u,a[100000],b[100000] ,n,m,v,min,max;
	scanf("%d",&n);
	for(i=0;i<n;i++)
	{
		scanf("%d",&m);
		for(u=0;u<m;u++)
		{
			scanf("%d",&a[u]);
		}
		min=a[0];
		for(u=0;u<m;u++)
		{
			if(a[u]<min)
			{
				min=a[u];
				b[u]=min;
			}
			else
			{
				b[u]=a[u];
			}
		}
		max=a[m-1];
		for(u=m-1;u>=0;u--)
		{
			if(a[u]>max)
			{
				max=a[u];
			}
			else
			{
				a[u]=max;
			}
		}
		max=0;
		for(u=0;u<m;u++)
		{
			if(a[u]-b[u]>max)
				max=a[u]-b[u];
		}
		printf("%d\n",max);
	}
	return 0;
}
